  Future<void> _loadData() async {
    try {
      setState(() => _isLoading = true);

      final supervisorId = _authService.currentUser?.uid ?? '';
      debugPrint('üìä Loading route statistics for supervisor: $supervisorId');

      // ÿßÿ≥ÿ™ÿÆÿØÿßŸÖ ÿßŸÑÿ∑ÿ±ŸäŸÇÿ© ÿßŸÑÿ®ÿ≥Ÿäÿ∑ÿ©
      final assignments = await _databaseService.getSupervisorAssignmentsSimple(supervisorId);

      if (assignments.isNotEmpty) {
        _assignment = assignments.first;
        var busRoute = _assignment!.busRoute;

        debugPrint('üöå Assignment busRoute: "$busRoute"');
        debugPrint('üöå Assignment busId: "${_assignment!.busId}"');

        // ÿ•ÿ∞ÿß ŸÉÿßŸÜ busRoute ŸÅÿßÿ±ÿ∫ÿå ÿßÿ≠ÿµŸÑ ÿπŸÑŸäŸá ŸÖŸÜ ÿ®ŸäÿßŸÜÿßÿ™ ÿßŸÑÿ®ÿßÿµ
        if (busRoute.isEmpty) {
          debugPrint('‚ö†Ô∏è busRoute is empty, fetching from bus data...');
          try {
            final bus = await _databaseService.getBusById(_assignment!.busId);
            if (bus != null) {
              busRoute = bus.route;
              // ÿ™ÿ≠ÿØŸäÿ´ ÿßŸÑŸÄ assignment ŸÖÿπ ÿßŸÑŸÄ busRoute ÿßŸÑÿµÿ≠Ÿäÿ≠
              _assignment = SupervisorAssignmentModel(
                id: _assignment!.id,
                supervisorId: _assignment!.supervisorId,
                supervisorName: _assignment!.supervisorName,
                busId: _assignment!.busId,
                busPlateNumber: _assignment!.busPlateNumber,
                busRoute: busRoute,
                direction: _assignment!.direction,
                status: _assignment!.status,
                assignedAt: _assignment!.assignedAt,
                assignedBy: _assignment!.assignedBy,
                assignedByName: _assignment!.assignedByName,
                notes: _assignment!.notes,
                isEmergencyAssignment: _assignment!.isEmergencyAssignment,
                originalSupervisorId: _assignment!.originalSupervisorId,
              );
              debugPrint('‚úÖ Got busRoute from bus: "$busRoute"');
            }
          } catch (e) {
            debugPrint('‚ùå Error getting bus data: $e');
          }
        }

        if (busRoute.isNotEmpty) {
          // ÿ™ÿ≠ŸÖŸäŸÑ ÿßŸÑÿ∑ŸÑÿßÿ® ÿ®ÿßÿ≥ÿ™ÿÆÿØÿßŸÖ ÿßŸÑÿ∑ÿ±ŸäŸÇÿ© ÿßŸÑÿ®ÿ≥Ÿäÿ∑ÿ©
          final students = await _databaseService.getStudentsByRouteSimple(busRoute);
          debugPrint('üë• Loaded ${students.length} students for route $busRoute');

          // ÿ™ÿ≠ŸÖŸäŸÑ ÿ∫Ÿäÿßÿ®ÿßÿ™ ÿßŸÑŸäŸàŸÖ
          final todayAbsences = await _databaseService.getTodayAbsencesForSupervisorSimple(supervisorId);
          debugPrint('üìÖ Loaded ${todayAbsences.length} today absences');

          // ÿ™ÿ≠ŸÖŸäŸÑ ÿ∫Ÿäÿßÿ®ÿßÿ™ ÿßŸÑÿ£ÿ≥ÿ®Ÿàÿπ
          final weekStart = DateTime.now().subtract(Duration(days: DateTime.now().weekday - 1));
          final weekAbsences = await _databaseService.getAbsencesInDateRangeSimple(
            supervisorId,
            weekStart,
            DateTime.now(),
          );
          debugPrint('üìÖ Loaded ${weekAbsences.length} week absences');

          setState(() {
            _students = students;
            _todayAbsences = todayAbsences;
            _weekAbsences = weekAbsences;
            _isLoading = false;
          });
        } else {
          debugPrint('‚ùå No valid busRoute found');
          setState(() => _isLoading = false);
        }
      } else {
        debugPrint('‚ö†Ô∏è No assignments found for supervisor');
        setState(() => _isLoading = false);
      }
    } catch (e) {
      debugPrint('‚ùå Error loading route statistics: $e');
      setState(() => _isLoading = false);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('ÿÆÿ∑ÿ£ ŸÅŸä ÿ™ÿ≠ŸÖŸäŸÑ ÿßŸÑÿ®ŸäÿßŸÜÿßÿ™: $e')),
        );
      }
    }
  }

